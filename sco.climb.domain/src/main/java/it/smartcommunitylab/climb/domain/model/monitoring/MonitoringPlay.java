public class MonitoringPlay {
	private long number;
	private long lastPlay;
	
	public long getNumber() {
		return number;
	}
	public void setNumber(long number) {
		this.number = number;
	}
	public long getLastPlay() {
		return lastPlay;
	}
	public void setLastPlay(long lastPlay) {
		this.lastPlay = lastPlay;
	} 
}
